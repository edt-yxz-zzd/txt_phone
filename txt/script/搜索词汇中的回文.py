from seed.io.may_open import open4wt, open4wt_err, open4rt

def is_palindrome_(word, /):
    return len(word) >= 3 and word == word[::-1]
def read5ipath_(ipath, /, *, encoding):
    with open(ipath, 'rt', encoding=encoding) as fin:
        for line in fin:
            for word in line.split(':'):
                word = word.strip()
                if word:
                    yield word

def 过滤词汇中的回文词(ipath, /, *, encoding='u8'):
    for word in read5ipath_(ipath, encoding=encoding):
        if is_palindrome_(word):
            print(word)

def is_word_reverseable_(word_set, word, /):
    return len(word) >= 2 and word[::-1] in word_set and word in word_set

def 收集词汇中的可反转词(*ipaths, encoding='u8', may_opath=None, force=False):
    word_set = {word for ipath in ipaths for word in read5ipath_(ipath, encoding=encoding)}
    word_ps = sorted({tuple(sorted([word, word[::-1]])) for word in word_set if is_word_reverseable_(word_set, word)})
    with open4wt(may_opath, force=force, encoding=encoding) as ofile:
        for w0, w1 in word_ps:
            print(f'{w0}:{w1}', file=ofile)





