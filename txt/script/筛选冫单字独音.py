def 迭代行析冫读音纟共享汉字扌(lines, /):
    for line in lines:
        hz, pinyins__str = line.strip().split(':')
        pinyins = tuple(pinyins__str.split(','))
        yield (hz, pinyins)
def 读取冫读音纟共享汉字扌(path='script/筛选冫单字独音.py..读音纟共享汉字.out.txt', encoding='utf8'):
    with open(path, 'rt', encoding=encoding) as ifile:
        yield from 迭代行析冫读音纟共享汉字扌(iter(ifile))
def 重构文件冫后置多音字扌(path='script/筛选冫单字独音.py..读音纟共享汉字.out.txt', encoding='utf8'):
    it = 读取冫读音纟共享汉字扌(path=path, encoding=encoding)
