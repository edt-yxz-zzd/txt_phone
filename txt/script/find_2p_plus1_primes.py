def mk_primes(*, case, directory4prime_tables):
    assert case in [1,2,3]
    encoding = 'ascii'
    total = 10**6
    #primes[0:1e6] = [2..~..15485863]
    #primes[1e6:2e6] = [15485867..~..32452843]
    #primes[2e6:3e6] = [32452867..~..49979687]
    basename_head_last_pair_pairs = [
        ('primes1.txt', (2,15485863))
        ,('primes2.txt', (15485867,32452843))
        ,('primes3.txt', (32452867,49979687))
        ]

    ls = []
    for basename, (head, last) in basename_head_last_pair_pairs[:case]:
        path = directory4prime_tables/basename
        ls.extend(mk_primes_(path, head=head, last=last, total=total, encoding=encoding))
    assert len(ls) == total*case
    assert ls[0] == basename_head_last_pair_pairs[0][1][0] == 2
    assert ls[-1] == basename_head_last_pair_pairs[case-1][1][1]
    return ls
def mk_primes_(path, *, head, last, total, encoding):
    with open(path, 'rt', encoding=encoding) as fin:
        for line in fin:
            break
        ls = []
        for line in fin:
            ls.extend(map(int, line.split()))
        assert len(ls) == total
        assert ls[0] == head
        assert ls[-1] == last

        return ls

def find_2p_plus1_primes(primes):
    primes = set(primes)
    p2pp = {}
    for pp in primes:
        if pp//2 in primes:
            p2pp[pp//2] = pp
    num_pairs = len(p2pp)
    s = set()
    lss = []
    for p, pp in sorted(p2pp.items()):
        if p in s: continue

        ls = [p, pp]
        while pp in p2pp:
            s.add(pp)
            p = pp
            pp = p2pp[p]
            ls.append(pp)
        lss.append(ls)
    return num_pairs, lss


def main4find_2p_plus1_primes(*, fout, case, directory4prime_tables):
    fprint = mk_fprint(fout)
    primes = mk_primes(case=case, directory4prime_tables=directory4prime_tables)
    fprint(f'input: primes=[{primes[0]}..~..{primes[-1]}]; len(primes)={len(primes)}')
    num_pairs, lss = find_2p_plus1_primes(primes)
    fprint(f'output: {num_pairs} pairs (p,2*p+1); len(lss)={len(lss)}')
    for ls in lss:
        fprint(ls)

    lss2 = sorted(lss, key=len)
    fprint(f'===========================')
    fprint(f'===========================')
    fprint(f'===========================')
    for ls in lss2:
        fprint(ls)


