from nn_ns.math_nn.prime2 import primes_lt


def show_II_primes_lt(N):
    ps = primes_lt(N)
    r = 1
    for i, p in enumerate(ps):
        r *= p
        s = '*'.join(map(str, ps[:i+1]))
        s = f'{r} = 0x{r:X} = {s!s}'
        print(s)
show_II_primes_lt(11**2)


