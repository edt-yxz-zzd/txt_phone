def 无序迭代所有回旋函数于元素数量(M, /):
    r'''
    -> 无序迭代<回旋函数>

    元素集合:[0..M-1] 即 uint%M
    回旋函数 :: Array<M, uint%M> === [uint%M]{len=M}
    #'''
    def swap(ls, i, j, /):
        if not i==j:
            ls[i], ls[j] = ls[j], ls[i]
    def 就地迭代所有回旋函数囗定义域(ls, L, out, /):
        #input: ls[:L]
        if L >= 1:
            last = ls[L-1]
            out[last] = last
            yield from 就地迭代所有回旋函数囗定义域(ls, L-1, out)
            out[last] = None
            for i in reversed(range(L-1)):
                swap(ls, i, L-2)
                last_prev = ls[L-2]
                out[last] = last_prev
                out[last_prev] = last
                yield from 就地迭代所有回旋函数囗定义域(ls, L-2, out)
                out[last] = None
                out[last_prev] = None
                swap(ls, i, L-2)
        else:
            assert L == 0
            yield
        return;yield

    def main(M, /):
        ls = [*range(M)]
        L = len(ls)
        out = [None]*M
        for _ in 就地迭代所有回旋函数囗定义域(ls, L, out):
            yield tuple(out)
    return main(M)

