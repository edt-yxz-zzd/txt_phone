from pathlib import Path
class BadBookFormatError(Exception):pass

def rename_file(*, from_fname, to_fname):
    if type(from_fname) is not Path:
        from_fname = Path(from_fname)
    if type(to_fname) is not Path:
        to_fname = Path(to_fname)

    if not from_fname.exists():
        raise FileNotFoundError(from_fname)
    elif to_fname.exists():
        if to_fname.samefile(from_fname):
            #do nothing
            pass
        else:
            raise FileExistsError(to_fname)
    else:
        from_fname.rename(to_fname)
    return

def xstt5_com__extract_book_title_from_txt__path(ifname, *, encoding):
    try:
        with open(ifname, 'rt', encoding=encoding) as fin:
            return xstt5_com__extract_book_title_from_txt__fin(fin)
    except BadBookFormatError:
        raise BadBookFormatError(f'not novel txt from xstt5.com: {ifname!r}')

def xstt5_com__extract_book_title_from_txt__fin(fin):
    book_title = None
    BOM = '\uFEFF'
    for book_title in fin:
        book_title = book_title.strip()
        #print(hex(ord(book_title[:1])))
        if book_title[:1] == BOM:
            book_title = book_title[1:]
        break

    if not book_title or not (book_title.startswith('《') and book_title.endswith('》')):
        if 0:
            if book_title is not None:
                print(book_title)
                print(book_title[:1], book_title[-1:])
                print(book_title.startswith('《'))
                print(book_title.endswith('》'))
        raise BadBookFormatError

    return book_title



